public class Boxing {
    public static void main(String[] args) {
        int a=100;
        Integer i2=a;
        //a is a primitive type we store a into i2 which is wrapper
        //primitive type --> wrapper class object autoboxing

        Integer i3=600;//autoboxing
        int z=i3;//unboxing its goes to from wrapper class object to primiitve type
        int x=new Integer(40);//unboxing

        int p=600;//just primitive type






    }
}
